void main() {
  //Relacionais
  //==, >=, <=, !=, >, <

  if (5 == 0) {
    print(true);
  } else {
    print(false);
  }

  if (5 >= 0 && 5 <= 6) {
    print(true);
  } else {
    print(false);
  }

  if (5 != 0 || 5 == 0) {
    print(true);
  } else {
    print(false);
  }

  if (5 > 0 || 5 < 0) {
    print(true);
  } else {
    print(false);
  }
}
